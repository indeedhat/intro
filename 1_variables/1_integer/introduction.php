<?php
/**
 * Integers are numbers
 * they are defined as just just by typing digits
 */

// assignment
$ten = 10;

// addition
$addition = $ten + 10; // 20

// subtraction
$sub = $ten - 5; // 5

// multiplication
$multi = $ten * 10; // 100

// division
$div = $ten / 2; // 5

// powers
$pow2 = $ten ** 2; // 100
$pow3 = $ten ** 3; // 1000

// remainder
$remainder = $ten % 3; // 1